# -*- coding: utf-8 -*-
"""
Central state module for the Aequitas-MAS.

This file defines the data structure that circulates among the agents in the LangGraph.
Adhering to the "Controlled Degradation" principle, financial metrics are represented
as `Optional[float]` and validated to avoid non-finite values (NaN/Inf).

Classes:
    GrahamMetrics: Pydantic schema for the Graham Agent's metrics.
    FisherAnalysis: Pydantic schema for the Fisher Agent's qualitative analysis.
    PortfolioWeight: Pydantic schema for optimized ticker weights.
    PortfolioOptimizationResult: Pydantic schema for deterministic optimizer output.
    CoreAnalysis: Pydantic schema for supervisor optimization output.
    AgentState: Pydantic BaseModel representing the complete graph state.
"""

import math
import operator
from datetime import date
from typing import Annotated, Any, List, Optional

from langchain_core.messages import BaseMessage
from langgraph.graph.message import add_messages
from pydantic import BaseModel, ConfigDict, Field, field_validator


# 1. PYDANTIC SCHEMAS (BOUNDARY VALIDATION)
# These schemas ensure that data generated by Tools or LLMs
# is mathematically correct before entering the state.


class GrahamMetrics(BaseModel):
    """
    Deterministic Value Investing metrics (Graham Agent).

    This class enforces the "Controlled Degradation" dogma by validating
    numeric values as finite floats and allowing missing metrics as `None`.
    """

    model_config = ConfigDict(frozen=True, populate_by_name=True)

    # Strict ticker validation for market standard compliance.
    ticker: str = Field(
        ...,
        description="O código de negociação do ativo na bolsa B3.",
        pattern=r"^[A-Z0-9]{5,6}$",
    )

    # Campos financeiros obrigatórios para a fórmula de Graham.
    vpa: Optional[float] = Field(
        description="Valor Patrimonial por Ação (VPA).",
    )
    lpa: Optional[float] = Field(
        description="Lucro por Ação (LPA).",
    )

    # Optional fields that depend on calculations or data availability.
    price_to_earnings: Optional[float] = Field(
        description="Índice Preço/Lucro (P/L).",
    )
    fair_value: Optional[float] = Field(
        description="Valor intrínseco calculado pela fórmula de Graham.",
    )
    margin_of_safety: Optional[float] = Field(
        description="Margem de segurança percentual.",
    )

    @field_validator(
        "vpa",
        "lpa",
        "price_to_earnings",
        "fair_value",
        "margin_of_safety",
        mode="before",
    )
    @classmethod
    def validate_finite_float(cls, v: Any) -> Optional[float]:
        """Safely coerces numeric values and rejects NaN/Inf."""
        if v is None:
            return None
        try:
            value = float(v)
        except (TypeError, ValueError) as exc:
            raise ValueError(f"Não foi possível converter o valor '{v}' para float.") from exc

        if not math.isfinite(value):
            raise ValueError(f"O valor '{v}' não é um número finito válido.")

        return value


class GrahamInterpretation(BaseModel):
    """Structured output boundary for the Graham interpreter agent."""

    model_config = ConfigDict(frozen=True)

    thesis: str = Field(
        ...,
        description="Investment thesis summarizing the Graham valuation outcome.",
    )
    fair_value_assessment: str = Field(
        ...,
        description="Assessment of the intrinsic value relative to market price.",
    )
    margin_of_safety_assessment: str = Field(
        ...,
        description="Assessment of the margin of safety adequacy.",
    )
    piotroski_assessment: Optional[str] = Field(
        default=None,
        description="Optional assessment of the Piotroski F-Score quality gate.",
    )
    altman_assessment: Optional[str] = Field(
        default=None,
        description="Optional assessment of the Altman Z-Score solvency gate.",
    )
    recommendation: str = Field(
        ...,
        description="Investment recommendation: buy, hold, or avoid.",
    )
    confidence: Optional[float] = Field(
        default=None,
        description="Confidence level between 0.0 and 1.0.",
    )

    @field_validator("confidence", mode="before")
    @classmethod
    def validate_finite_confidence(cls, v: Any) -> Optional[float]:
        """Degrade non-finite confidence to None."""
        if v is None:
            return None
        try:
            value = float(v)
        except (TypeError, ValueError):
            return None
        if not math.isfinite(value):
            return None
        return value


