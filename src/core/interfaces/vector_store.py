class NullVectorStore:
    """
    Null Object Pattern implementation of VectorStorePort.

    Used as a safe default when OpenSearch is not configured (local/offline
    mode). Returns an empty list on every call, triggering Controlled
    Degradation in the Macro Agent without raising exceptions.

    This class satisfies VectorStorePort structurally — no inheritance needed.
    """

    def search_macro_context(
        self,
        query: str,
        as_of_date: date,
        top_k: int = 5,
    ) -> list[VectorSearchResult]:
        """Return empty results, triggering Controlled Degradation downstream."""
        del query, as_of_date, top_k
        return []
